public class Octal {
  public static void main(String[] args) {

     int a = 013;//8進数で代入
     int b = 0xb;//16進数で代入
     int c = 10 ; //変数c=10を定義
     float d = c;

     /*
      8進数は数値の前に「0」
      16進数は数値の前に「0x」
      を記載
     */

     System.out.println(a);
     System.out.println(b);
     System.out.println(d);
 }
}
